package nl.samen.namen.entities;

public class Voornaam {

    private String naam;
    private String mannen;
    private String vrouwen;

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    public String getMannen() {
        return mannen;
    }

    public void setMannen(String mannen) {
        this.mannen = mannen;
    }

    public String getVrouwen() {
        return vrouwen;
    }

    public void setVrouwen(String vrouwen) {
        this.vrouwen = vrouwen;
    }
}
